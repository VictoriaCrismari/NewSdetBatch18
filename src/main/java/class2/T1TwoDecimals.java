package class2;

public class T1TwoDecimals {
    public static void main(String[] args) {
        double num1=20;
        double num2=10;
        System.out.println("The Adition of 2 numbers "+num1+" and "+num2+" is equal to "+(num1+num2));
        System.out.println("The Substraction of 2 numbers "+num1+" and "+num2+" is equal to "+(num1-num2));
        System.out.println("The Multiplication of 2 numbers "+num1+" and "+num2+" is equal to "+(num1*num2));
        System.out.println("The Division of 2 numbers "+num1+" and "+num2+" is equal to "+(num1/num2));
    }
}
